    public ArrayList<User> getarraylist()
    {
    ArrayList<User> ar=new ArrayList();
    String s="Select * from issue";
    try{
    ps=c.prepareStatement(s);
    rs=ps.executeQuery();
    while(rs.next())
    {
    User list=new User(rs.getString(8),rs.getString(9),rs.getString(1),rs.getString(2),rs.getString(11));
    ar.add(list);
    }}catch(Exception e){JOptionPane.showMessageDialog(null, "Exception"+e);}
    return ar;
    
    
    }
    
    
    public void setTable()
    {
        try
        {
    ArrayList<User> list=getarraylist();
    DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
    Object[] obj=new Object[5];
    for(int i=0;i<list.size();i++)
    {
    obj[0]=list.get(0).getStudentname();
    obj[1]=list.get(1).getRollno();
    obj[2]=list.get(2).getBook();
    obj[3]=list.get(3).getBookName();
    //obj[4]=list.get(4).getDateofIssue();
    model.addRow(obj);
    
    
    }
    
    
    
        }
        catch(Exception e)
        {JOptionPane.showMessageDialog(null,"Error"+e);}
    }
    

